# Функция, которая принимает на вход целое число n
# и выводит треугольник Серпинского размера n
def serpinski_triangle(n):
  # Базовый случай: если n равно 0, то ничего не выводим
  if n == 0:
    return

  # Иначе, выводим треугольник размера n-1
  serpinski_triangle(n-1)

  # Выводим треугольник размера n-1
  serpinski_triangle(n-1)

  # Выводим n строк треугольника
  for i in range(n):
    # В каждой строке выводим 2^n - 1 звездочек,
    # отступая слева на 2^(n-1) - 1 табуляций
    print('\t' * (2**(n-1) - 1) + '*' * (2**n - 1))

# Тестируем функцию с n = 3
serpinski_triangle(5)